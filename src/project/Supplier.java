    protected void datatable() {
        Object[] Baris ={"id_supplier","nama_supplier","no_telp","alamat"};
        tabmode = new DefaultTableModel(null, Baris);
        tabel2.setModel(tabmode);
        String sql = "select * from supplier"; 
        try {
            java.sql.Statement stat = kon.createStatement();
            ResultSet hasil = stat.executeQuery(sql);
            while(hasil.next()){
                String a = hasil.getString("id_supplier");
                String b = hasil.getString("nama_supplier");
                String c = hasil.getString("no_telp");
                String d = hasil.getString("alamat");
              String[] data={a,b,c,d,};
                tabmode.addRow(data);
            }
        } catch (Exception e){}
    }
