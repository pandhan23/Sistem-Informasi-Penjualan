public void kode_supplier(){
      String kode="SP-000";
      int i=0;
    try{
        
        Connection kon=DriverManager.getConnection("jdbc:mysql://localhost:3306/penjualan_makanan","root","");
        Statement stat = kon.createStatement();
        ResultSet rs = stat.executeQuery("select id_supplier from supplier");
        
        while(rs.next()){
            kode = rs.getString("id_supplier");
        }
        kode = kode.substring(4);
        i = Integer.parseInt(kode)+1;
        kode = "00"+i;
        kode = "SP-"+kode.substring(kode.length()-3);
        tId.setText(kode);
       
    }catch(SQLException e){
        System.out.println(e.getMessage());
    }
        
    }
