    private void jHpActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jHpActionPerformed
       int ok = JOptionPane.showConfirmDialog(null, "Yakin untuk di hapus ?","Konfirmasi Dialog", JOptionPane.YES_NO_OPTION);
       if (ok==0){
           String sql = "delete from admin where ussername ='"+jUser.getText()+"'";
           try {
               PreparedStatement stat = kon.prepareStatement(sql);
               stat.executeUpdate();
               JOptionPane.showMessageDialog(null, "Data berhasil dihapus");
               datatable();
               kosong();
           }catch (SQLException e){
               JOptionPane.showMessageDialog(null, "Data gagal dihapus"+e);
           }
       }
    }//GEN-LAST:event_jHpActionPerformed

    private void jSpActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jSpActionPerformed
        String sql = "insert into admin (username,password) values (?,?)";
        try {
            PreparedStatement stat = kon.prepareStatement(sql);
            stat.setString(1, jUser.getText());
            
            stat.setString(2, jPw.getText());
       
            
            
            stat.executeUpdate();
            kosong();
            datatable();
            JOptionPane.showMessageDialog(null, "Data Berhasil Disimpan");
           
        }catch (SQLException e){
            JOptionPane.showMessageDialog(null, "Data Gagal Disimpan"+e);
        }   
    }//GEN-LAST:event_jSpActionPerformed

